#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			a;
	unsigned char	*i;

	i = (unsigned char *)s;
	a = 0;
	while (n > a)
	{
		i[a] = 0;
		a++;
	}
	s = i;
}
