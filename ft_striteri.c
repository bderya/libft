#include "libft.h"

void	ft_striteri(char *s, char ( *f)(unsigned int, char))
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		s[i] = (*f)(i, s[i]);
		i++;
	}
	return (s);
}
